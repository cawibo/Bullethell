int btn_pressed(int b) {
	return 1 & (getbtns() >> (b-1));
}


void display_screen(const uint8_t *data) {
  int i;

  for(i = 0; i < 4; i++) {
    display_image(32*i, &data[128*i]);
  }
}

uint8_t within_bounds(int x, int y) {
    return (x > 0 && x < 127 && y > 0 && y < 32);
}

void clear_screen() {
  int i;
  for(i = 0; i < 512; i++) screen[i] = 0;
}

void enable_pixel(int x, int y) {
  int bit = y%8;
  screen[translate_coordinates(x, y)] |= 1 << bit;
}

void disable_pixel(int x, int y) {
  int bit = y%8;
  screen[translate_coordinates(x, y)] &= ~(1 << bit);
}

int translate_coordinates(int x, int y) {
  int sect;
  int squareX, squareY;
  int bar, bit;

  sect = x/32;
  squareX = (x%32)/8;
  squareY = y/8;
  bar = x%8;

  return 128*sect + 8*4*squareY + 8*squareX + bar;
}

