/*
  Returns the state of respective button.
*/
int btn_2_pressed(void) {return btn_pressed(1);}
int btn_3_pressed(void) {return btn_pressed(2);}
int btn_4_pressed(void) {return btn_pressed(3);}

/*
	Only supports button 2 through 4.
*/
int btn_pressed(int b) {
	return 1 & (getbtns() >> (b-1));
}

/*
  Draws the bitmap array data on the LED screen.
  Utilizes the mipslab display_image function.
*/
void display_screen(const uint8_t *data) {
  int i;
  for(i = 0; i < 4; i++) {
    display_image(32*i, &data[128*i]);
  }
}

/*
  Returns a truth value for whether the coordinate is within bounds or not.
*/ 
uint8_t within_bounds(int x, int y) {
    return (x > 0 && x < 127 && y > 0 && y < 32);
}

/*
  Clears the bitmap array by writing zeroes.
*/
void clear_screen() {
  int i;
  for(i = 0; i < 512; i++) screen[i] = 0;
}

/*
  Sets the given coordinate on the bitmap array to one.
*/
void enable_pixel(int x, int y) {
  int bit = y%8;
  screen[translate_coordinates(x, y)] |= 1 << bit;
}

/*
  Sets the given coordinate on the bitmap array to zero.
*/
void disable_pixel(int x, int y) {
  int bit = y%8;
  screen[translate_coordinates(x, y)] &= ~(1 << bit);
}

/*
  Translate the given coordinate to fit the mipslab drawing function.
*/
int translate_coordinates(int x, int y) {
  int sect;
  int squareX, squareY;
  int bar, bit;

  sect = x/32;
  squareX = (x%32)/8;
  squareY = y/8;
  bar = x%8;

  return 128*sect + 8*4*squareY + 8*squareX + bar;
}

