// The initial state; initial is a SPLASH screen.
State state = INITIAL;
// The score counter
uint8_t score = 0;

/*
	Returns a deterministically chosen "random" value.
*/
int pr = 1;
int new_rnd() {
    pr = (37 * pr + 7) % 4721;
    return (int)pr;
}

/*
	Main game loop
*/
void run() {
    while( 1 ) {
		if(state == INITIAL) {
			state = MENU;
			
			display_string(0, "BULLETHELL");
			display_string(1, "GOTY Edition");
			display_update();
			
			delay(3000);
		}
        if(state == MENU) {
            init_menu();
            run_menu();
            clear_menu();
        }
        if(state == GAME) {
            init_game();
            run_game();
            clear_game();
        }
        if(state == CREDITS) {
            init_credits();
            run_credits();
            clear_credits();
        }
		if(state == OPTIONS) {
			run_options();
		}
    }
}

// if 1, will blip inverted colors on screen.
uint8_t inverted_colors = 0;
void run_options() {
	display_string(0, "");
	display_string(1, "Btn4: Toggle");
	display_string(2, "Btn2: Leave");
	display_update();
	
	while(state == OPTIONS) {
		if(btn_4_pressed()) {
			inverted_colors = ~(1 & inverted_colors);
			break;
		}
		if(btn_2_pressed()) break;
	}
	state = MENU;
}

// The size of the menu, needs to be set before runtime.
uint8_t menu_size = 3;
// array of menu_item structs; first element is its showstring
// second elements is its target state. 
