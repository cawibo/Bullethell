void move_enemies() {
    int i;
    struct enemy* ptr = enemies;
    for(i=0; i < enemies_size; i++, ptr++) {
        move_left_enemy(ptr);
    }
}

uint8_t check_collision_pixel(int x, int y) {
    return (collision[x][y] & 1);
}

uint8_t check_collision_body(int x, int y, int width, int height) {
    int i, j;
    for(i = x - width; i <= x + width; i++) {
        for(j = y - height; j <= y + height; j++) {
            if(check_collision_pixel(i, j)) return 1;
        }
    }
    return 0;
}

void update_enemies() {
    int i;
    struct enemy* ptr = enemies;
    for(i=0; i < enemies_size; i++, ptr++) {
        update_enemy(ptr);
    }
}

void blip_enemies() {
    int i;
    struct enemy* ptr = enemies;
    for(i=0; i < enemies_size; i++, ptr++) {
        blip_enemy(*ptr);
    }
}

void clear_enemies() {
	int i;
	for(i = 0; i < enemies_size; i++) {
		enemies[i].x = 128 + i*12;
		enemies[i].y = 6 + i*6;
		enemies[i].speed = 4;
		enemies[i].width = 3;
		enemies[i].height = 3;
	}
}

