/*
  clear function for menu state.  
*/
void clear_menu() {
    
}

/*
  Set initial values for game state.  
*/
void init_game() {
	clear_game();
	clear_score();
}

/*
  run loop for game state  
*/
void run_game() {
    uint8_t counter = 0;
    while(state == GAME) {
        // clear();
        clear_screen();
        clear_collision();

        // // update();
        update_enemies();
        update_player(&p);

        // // blip();
        blip_player(p);
        blip_enemies();

        // // draw();
		if(inverted_colors) {
			uint32_t i;
			uint32_t *ptr = ((uint32_t*)screen);
			for(i = 0; i < 128; i++, ptr++)
				*ptr = ~*ptr;
		}
        display_screen(screen);

        counter = (++counter % 5);
        if(counter == 0) score += 1;
        PORTE = score; // sets the led light value
        delay(200);
        if(btn_3_pressed() || has_ended()) break;
    }
    state = MENU;
}

