void show_menu() {
    uint8_t i;
    for(i = 0; i < menu_size; i++) {
        if(selected_menu_item == i) menu_items[i].name[1] = '*';
        else menu_items[i].name[1] = ' ';
        display_string(i, menu_items[i].name);
    };
    display_update();
}

void update_menu() {
    if(btn_4_pressed() && selected_menu_item > 0) selected_menu_item -= 1;
    if(btn_2_pressed() && selected_menu_item < menu_size-1) selected_menu_item += 1;
}

void stop_game() {
    clear();
    state = MENU;
}

uint8_t has_ended() {
    return check_collision_player(&p);
}

void clear() {
    clear_screen();
    clear_collision();
}

void update() {
    update_enemies();
    update_player(&p);
}

void blip() {
    blip_player(p);
    blip_enemies();
}

void draw() {
    display_screen(screen);
    display_update();
}

void move_enemies() {
    int i;
    struct enemy* ptr = enemies;
    for(i=0; i < 10; i++, ptr++) {
        move_left_enemy(ptr);
    }
}

uint8_t check_collision_pixel(int x, int y) {
    return (collision[x][y] & 1);
}

uint8_t check_collision_body(int x, int y, int width, int height) {
    int i, j;
    for(i = x - width; i <= x + width; i++) {
        for(j = y - height; j <= y + height; j++) {
            if(check_collision_pixel(i, j)) return 1;
        }
    }
    return 0;
}

void update_enemies() {
    int i;
    struct enemy* ptr = enemies;
    for(i=0; i < 10; i++, ptr++) {
        update_enemy(ptr);
    }
}

void blip_enemies() {
    int i;
    struct enemy* ptr = enemies;
    for(i=0; i < 10; i++, ptr++) {
        blip_enemy(*ptr);
    }
}

void clear_collision() {
    int i;
    int j;
    for(i = 0; i < 128; i++) for(j = 0; j < 32; j++) collision[i][j] = 0;
}

