void run() {
    while( 1 ) {
        clear();
        update();
        blip();
        draw();

        delay(200);

        if (has_ended()) break;
    }


}

uint8_t has_ended() {
    return check_collision_player(&p);
}

void clear() {
    clear_screen();
    clear_collision();
}

void update() {
    update_enemies();
    update_player(&p);
}

void blip() {
    blip_player(p);
    blip_enemies();
}

void draw() {
    display_screen(screen);
}

void move_enemies() {
    int i;
    struct enemy* ptr = enemies;
    for(i=0; i < 10; i++, ptr++) {
        move_left_enemy(ptr);
    }
}

uint8_t check_collision_pixel(int x, int y) {
    return (collision[x][y] & 1);
}

uint8_t check_collision_body(int x, int y, int width, int height) {
    int i, j;
    for(i = x - width; i <= x + width; i++) {
        for(j = y - height; j <= y + height; j++) {
            if(check_collision_pixel(i, j)) return 1;
        }
    }
    return 0;
}

void update_enemies() {
    int i;
    struct enemy* ptr = enemies;
    for(i=0; i < 10; i++, ptr++) {
        update_enemy(ptr);
    }
}

void blip_enemies() {
    int i;
    struct enemy* ptr = enemies;
    for(i=0; i < 10; i++, ptr++) {
        blip_enemy(*ptr);
    }
}

